void write(const std::vector<double> &values,
           const std::string &path)
{
    std::ofstream out(path);
    bool newline = false;
    for(auto v : values) {
        out << v << ",";
        if(newline)
            out << std::endl;
        newline = !newline;
    }
    out.close();
}

